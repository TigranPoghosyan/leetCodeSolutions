package leetCodeSolutions.easyProblems.linkedListCycle141;

public class ListNode {
    int val;

    ListNode nextNode;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode nextNode) {
        this.val = val;
        this.nextNode = nextNode;
    }
}
